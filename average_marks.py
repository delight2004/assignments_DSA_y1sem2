def physics():
    a=input("Physics Assignment marks:")
    a_num=int(a)
    c=input("Physics Coursework marks:")
    c_num=int(c)
    m=input("Physics Mid term marks:")
    m_num=int(m)
    e=input("Physics End of term marks:")
    e_num=int(e)

    s=a_num+c_num+m_num+e_num
    average=s/4
    print("Average for Physics",average)
physics()
